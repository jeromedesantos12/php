<?php 
// Pengulangan

// for
for($i = 0; $i < 5; $i++) {
	echo "Hello World! <br>";
}

echo "<br>";

// while
$j = 10;
while($j < 5) {
	echo "Hello World! <br>";
	$j++;
}

echo "<br>";

// do.. while
$k = 10;
do {
	echo "Hello World! <br>";
	$k++;
} while($k < 5);
// perbedaan : ketika kondisinya bernilai false, maka blok akan dijalankan 1x

// foreach : pengulangan khusus array

 ?>